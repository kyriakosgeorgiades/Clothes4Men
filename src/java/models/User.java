//User model
public class User {

    public User() {
    }
    ;
    private int ID;
    private String role;
    private String username;
    private String email;

    public User(int ID, String role, String username, String email) {
        this.ID = ID;
        this.role = role;
        this.username = username;
        this.email = email;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
